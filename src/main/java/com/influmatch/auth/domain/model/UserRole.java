package com.influmatch.auth.domain.model;

/**
 * Enumeración que define los roles posibles en la aplicación.
 * BRAND: Usuario representando una marca o empresa
 * INFLUENCER: Usuario representando un influencer o creador de contenido
 */
public enum UserRole {
    /**
     * Representa una marca o empresa que busca colaboraciones
     */
    BRAND,

    /**
     * Representa un influencer o creador de contenido
     */
    INFLUENCER
} 